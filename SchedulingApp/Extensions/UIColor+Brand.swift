import Foundation
import UIKit

extension UIColor {
    
    class func yinzGray() -> UIColor {
        return UIColor(red: 153/255, green: 153/255, blue: 153/255, alpha: 1)
    }
    
    class func yinzDarkGreen() -> UIColor {
        return UIColor(red: 36/255, green: 78/255, blue: 87/255, alpha: 1)
    }
}
